#include <iostream>
using namespace std;

int main() {
    // Integer variable
    int age = 25;
    
    // Floating-point variable
    float weight = 68.5;
    
    // Character variable
    char grade = 'A';
    
    // Boolean variable
    bool isStudent = true;
    
    // String variable (C++ string)
    string name = "John Doe";
    
    // Displaying the values of variables
    cout << "Name: " << name << endl;
    cout << "Age: " << age << " years" << endl;
    cout << "Weight: " << weight << " kg" << endl;
    cout << "Grade: " << grade << endl;
    cout << "Is student: " << (isStudent ? "Yes" : "No") << endl;
    
    return 0;
}
